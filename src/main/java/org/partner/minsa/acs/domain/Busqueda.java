package org.partner.minsa.acs.domain;

import java.sql.Timestamp;

import com.fasterxml.jackson.annotation.JsonInclude;



@JsonInclude(JsonInclude.Include.NON_NULL)
public class Busqueda extends Pagination {
	 

 
	private String region;
	private String red;
	private String microred;
	private String es;
	private String periodo1;
	private String periodo2;
	private Timestamp fecha_consulta;
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getRed() {
		return red;
	}
	public void setRed(String red) {
		this.red = red;
	}
	public String getMicrored() {
		return microred;
	}
	public void setMicrored(String microred) {
		this.microred = microred;
	}
	public String getEs() {
		return es;
	}
	public void setEs(String es) {
		this.es = es;
	}
	public String getPeriodo1() {
		return periodo1;
	}
	public void setPeriodo1(String periodo1) {
		this.periodo1 = periodo1;
	}
	public String getPeriodo2() {
		return periodo2;
	}
	public void setPeriodo2(String periodo2) {
		this.periodo2 = periodo2;
	}
	public Timestamp getFecha_consulta() {
		return fecha_consulta;
	}
	public void setFecha_consulta(Timestamp fecha_consulta) {
		this.fecha_consulta = fecha_consulta;
	}

   
}